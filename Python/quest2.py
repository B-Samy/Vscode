



def switch(value):
    if value == 1:
        return "case 1"
    elif value == 2:
        return "case 2"
    elif value == 3:
        return "case 3"
    elif value == 4:
        return "case 4"
    elif value == 5:
        return "case 5"
    
    else:
        return None
    

return_result = switch(5)
